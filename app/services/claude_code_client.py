from __future__ import annotations

import json
import os
import shutil
import subprocess
from pathlib import Path
from typing import Any, Callable

from app.core.config import Settings
from app.services.orchestration_settings_store import OrchestrationSettings


class ClaudeCodeClientError(RuntimeError):
    pass


Runner = Callable[..., subprocess.CompletedProcess[str]]


class ClaudeCodeWorkspace:
    def __init__(
        self,
        *,
        project_root: Path,
        agents_source: Path,
        skills_source: Path,
        global_claude_root: Path | None = None,
    ) -> None:
        self.project_root = project_root
        self.local_claude_root = project_root / ".claude"
        self.global_claude_root = global_claude_root or Path(os.getenv("CLAUDE_CONFIG_DIR", "~/.claude")).expanduser()
        self.agents_source = agents_source
        self.skills_source = skills_source

    def ensure_project_assets(self) -> dict[str, Any]:
        self.local_claude_root.mkdir(parents=True, exist_ok=True)
        marker = self.local_claude_root / ".borg-universe-sync"
        generated = marker.exists()
        if not generated:
            marker.write_text(
                "Generated by Borg Universe from project agents/ and skills/.\n",
                encoding="utf-8",
            )
            generated = True

        return {
            "project_root": str(self.project_root),
            "local_claude_root": str(self.local_claude_root),
            "global_claude_root": str(self.global_claude_root),
            "global_agents_exists": (self.global_claude_root / "agents").is_dir(),
            "local_agents_exists": (self.local_claude_root / "agents").is_dir(),
            "global_skills_exists": (self.global_claude_root / "skills").is_dir(),
            "local_skills_exists": (self.local_claude_root / "skills").is_dir(),
            "agents": self._sync_agents(generated),
            "skills": self._sync_skills(generated),
        }

    def _sync_agents(self, generated: bool) -> list[str]:
        target_root = self.local_claude_root / "agents"
        target_root.mkdir(parents=True, exist_ok=True)
        synced: list[str] = []
        if not self.agents_source.is_dir():
            return synced
        for source in sorted(self.agents_source.glob("*.md")):
            if source.name.lower() == "readme.md":
                continue
            target = target_root / source.name
            if self._copy_file(source, target, generated):
                synced.append(str(target.relative_to(self.project_root)))
        return synced

    def _sync_skills(self, generated: bool) -> list[str]:
        target_root = self.local_claude_root / "skills"
        target_root.mkdir(parents=True, exist_ok=True)
        synced: list[str] = []
        if not self.skills_source.is_dir():
            return synced
        for source_dir in sorted(path for path in self.skills_source.iterdir() if path.is_dir()):
            if not (source_dir / "SKILL.md").is_file():
                continue
            target_dir = target_root / source_dir.name
            for source in sorted(path for path in source_dir.rglob("*") if path.is_file()):
                if any(part in {".git", "__pycache__"} for part in source.parts):
                    continue
                target = target_dir / source.relative_to(source_dir)
                if self._copy_file(source, target, generated):
                    synced.append(str(target.relative_to(self.project_root)))
        return synced

    def _copy_file(self, source: Path, target: Path, generated: bool) -> bool:
        if target.exists() and not generated:
            return False
        target.parent.mkdir(parents=True, exist_ok=True)
        if target.exists() and target.read_bytes() == source.read_bytes():
            return False
        shutil.copy2(source, target)
        return True


class ClaudeCodeClient:
    def __init__(
        self,
        *,
        settings: Settings,
        orchestration: OrchestrationSettings,
        workspace: ClaudeCodeWorkspace | None = None,
        runner: Runner | None = None,
        timeout_seconds: float | None = None,
        mcp_config_json: str | None = None,
    ) -> None:
        self.settings = settings
        self.orchestration = orchestration
        self.workspace = workspace or ClaudeCodeWorkspace(
            project_root=_infer_project_root(settings),
            agents_source=settings.agents_root,
            skills_source=settings.skills_root,
        )
        self.runner = runner or subprocess.run
        self.timeout_seconds = timeout_seconds or float(os.getenv("LLM_TIMEOUT_SECONDS", os.getenv("CLAUDE_TIMEOUT_SECONDS", "1800")))
        self.mcp_config_json = mcp_config_json

    def ensure_project_assets(self) -> dict[str, Any]:
        return self.workspace.ensure_project_assets()

    def send_prompt(self, prompt: str) -> dict[str, Any]:
        assets = self.ensure_project_assets()
        command = self._build_command(prompt)
        env = self._build_env()
        try:
            completed = self.runner(
                command,
                cwd=str(self.workspace.project_root),
                env=env,
                capture_output=True,
                text=True,
                timeout=self.timeout_seconds,
            )
        except FileNotFoundError as exc:
            raise ClaudeCodeClientError("Claude Code executable was not found. Set CLAUDE_EXECUTABLE or install claude.") from exc
        except subprocess.TimeoutExpired as exc:
            raise ClaudeCodeClientError(f"Claude Code timed out after {self.timeout_seconds:g} seconds") from exc

        stdout = completed.stdout.strip()
        stderr = completed.stderr.strip()
        if completed.returncode != 0:
            detail = stderr or stdout or f"exit code {completed.returncode}"
            raise ClaudeCodeClientError(f"Claude Code failed: {detail}")

        parsed = _parse_json_object(stdout)
        content = _extract_content(parsed) if parsed else stdout
        return {
            "content": content,
            "response": parsed or {"stdout": stdout},
            "stdout": stdout,
            "stderr": stderr,
            "command": self._debug_command(),
            "assets": assets,
        }

    def _model_name(self) -> str:
        return (
            self.orchestration.local_model.model_name
            or self.orchestration.agent_selection.agent_name
            or "borg-cpu"
        )

    def _build_env(self) -> dict[str, str]:
        env = os.environ.copy()
        local_model = self.orchestration.local_model
        base_url = f"http://{local_model.ip_address}:{local_model.port}"
        env["ANTHROPIC_BASE_URL"] = base_url
        env["ANTHROPIC_API_KEY"] = local_model.api_key or "lmstudio"
        env["OPENAI_BASE_URL"] = f"{base_url}/v1"
        env["OPENAI_API_KEY"] = local_model.api_key or "lmstudio"
        env["BORG_AGENTS_ROOT"] = str(self.settings.agents_root)
        env["BORG_SKILLS_ROOT"] = str(self.settings.skills_root)
        env["BORG_CLAUDE_PROJECT_ROOT"] = str(self.workspace.project_root)
        return env

    def _build_command(self, prompt: str) -> list[str]:
        command = [
            os.getenv("CLAUDE_EXECUTABLE", "claude"),
            "--bare",
            "--no-session-persistence",
        ]
        _append_permission_args(command)
        if self.mcp_config_json:
            command.extend(["--mcp-config", self.mcp_config_json])
        command.extend([
            "-p",
            prompt,
            "--model",
            self._model_name(),
            "--output-format",
            "json",
        ])
        return command

    def _debug_command(self) -> list[str]:
        command = [
            os.getenv("CLAUDE_EXECUTABLE", "claude"),
            "--bare",
            "--no-session-persistence",
        ]
        _append_permission_args(command)
        if self.mcp_config_json:
            command.extend(["--mcp-config", "<mcp-config>"])
        command.extend([
            "-p",
            "<prompt>",
            "--model",
            self._model_name(),
            "--output-format",
            "json",
        ])
        return command


def _infer_project_root(settings: Settings) -> Path:
    configured = os.getenv("CLAUDE_PROJECT_ROOT")
    if configured:
        return Path(configured).expanduser().resolve()

    agents_root = settings.agents_root.expanduser().resolve()
    skills_root = settings.skills_root.expanduser().resolve()
    if agents_root.name == "agents" and skills_root.name == "skills" and agents_root.parent == skills_root.parent:
        return agents_root.parent

    borg_root = settings.borg_root.expanduser().resolve()
    if borg_root.name.upper() == "BORG":
        return borg_root.parent

    return Path.cwd().resolve()


def _can_bypass_permissions_as_current_user() -> bool:
    geteuid = getattr(os, "geteuid", None)
    if geteuid is None:
        return True
    return geteuid() != 0


def _append_permission_args(command: list[str]) -> None:
    permission_mode = os.getenv("CLAUDE_PERMISSION_MODE", "bypassPermissions").strip()
    if permission_mode == "bypassPermissions":
        if _can_bypass_permissions_as_current_user():
            command.append("--dangerously-skip-permissions")
        else:
            root_permission_mode = os.getenv("CLAUDE_ROOT_PERMISSION_MODE", "acceptEdits").strip()
            if root_permission_mode:
                command.extend(["--permission-mode", root_permission_mode])
        return
    if permission_mode:
        command.extend(["--permission-mode", permission_mode])


def _parse_json_object(value: str) -> dict[str, Any]:
    try:
        parsed = json.loads(value)
    except json.JSONDecodeError:
        return {}
    return parsed if isinstance(parsed, dict) else {}


def _extract_content(payload: dict[str, Any]) -> str:
    for key in ("result", "content", "text", "message"):
        value = payload.get(key)
        if isinstance(value, str):
            return value
    messages = payload.get("messages")
    if isinstance(messages, list):
        parts: list[str] = []
        for message in messages:
            if isinstance(message, dict) and isinstance(message.get("content"), str):
                parts.append(message["content"])
        if parts:
            return "\n".join(parts)
    return json.dumps(payload, ensure_ascii=False)
